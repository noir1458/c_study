//relational
#include <stdio.h>
int main(void) {
 int x = 3, y = 4;

 printf("%d == %d 의 결과 : %d\n", x, y, x == y);
 printf("%d != %d 의 결과 : %d\n", x, y, x != y);
 printf("%d > %d 의 결과 : %d\n", x, y, x > y);
 printf("%d < %d 의 결과 : %d\n", x, y, x < y);
 printf("%d >= %d 의 결과 : %d\n", x, y, x >= y);
 printf("%d <= %d 의 결과 : %d\n", x, y, x <= y);

 return 0;
}
